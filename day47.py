# Create a program that imports the math module and uses its functions

import math

print(f'9.2 rounded up: {math.ceil(9.2)}')
print(f'9.6 rounded down: {math.floor(9.6)}')
print(f'Absolute value of -7: {math.fabs(-7)}')
print(f'10 factorial: {math.factorial(10)}')
print(f'Remainder of 5 divided 4: {math.fmod(5, 4)}')
print(f'Greatest common divisor of 21 and 96: {math.gcd(21, 96)}')
print(f'5 to the power of 3: {math.pow(5, 3)}')
print(f'Square root of 25: {math.sqrt(25)}')
print(f'Fractional and integer parts of 10.4: {math.modf(10.4)}')
print(f'Hypotenuse of triangle with sides of 3 and 4: {math.hypot(3,4)}')